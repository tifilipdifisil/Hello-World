<h1>Storm Trooper</h1>
<p>Stormtroopers are elite shock troops fanatically loyal to the Empire and impossible to sway from the Imperial cause.
 They wear imposing white armor, which offers a wide range of survival equipment and temperature controls to allow the 
soldiers to survive in almost any environment.
Stormtroopers wield blaster rifles and pistols with great skill, and attack in hordes to overwhelm their enemies. 
Along with standard stormtroopers, the Empire has organized several specialized units, including snowtroopers and scout troopers.</p>
